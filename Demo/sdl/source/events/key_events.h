	enum Modifier : unsigned {
		NONE = KMOD_NONE,
		LEFT_SHIFT = KMOD_LSHIFT,
		RIGHT_SHIFT = KMOD_RSHIFT,
		LEFT_CTRL = KMOD_LCTRL,
		RIGHT_CTRL = KMOD_RCTRL,
		LEFT_ALT = KMOD_LALT,
		RIGHT_ALT = KMOD_RALT
	};
	FLAGSET(Modifier, ModifierFlags);

	struct KeyEvent {
		bool key_down;
		Key key;
		Scancode scancode;
		int window_id;
		ModifierFlags modifier;

		static ModifierFlags get_flags(int mods) {
			ModifierFlags r{ Modifier::NONE };
			r |= (mods & Modifier::LEFT_SHIFT) > 0 ? Modifier::LEFT_SHIFT : Modifier::NONE;
			r |= (mods & Modifier::RIGHT_SHIFT) > 0 ? Modifier::RIGHT_SHIFT : Modifier::NONE;
			r |= (mods & Modifier::LEFT_CTRL) > 0 ? Modifier::LEFT_CTRL : Modifier::NONE;
			r |= (mods & Modifier::RIGHT_CTRL) > 0 ? Modifier::RIGHT_CTRL : Modifier::NONE;
			r |= (mods & Modifier::LEFT_ALT) > 0 ? Modifier::LEFT_ALT : Modifier::NONE;
			r |= (mods & Modifier::RIGHT_ALT) > 0 ? Modifier::RIGHT_ALT : Modifier::NONE;

			return r;
		}

		static Uint16 set_flags(ModifierFlags f) {
			Uint16 r = 0;
			r |= f.Test(Modifier::LEFT_SHIFT) ? Modifier::LEFT_SHIFT : Modifier::NONE;
			r |= f.Test(Modifier::RIGHT_SHIFT) ? Modifier::RIGHT_SHIFT : Modifier::NONE;
			r |= f.Test(Modifier::LEFT_CTRL) ? Modifier::LEFT_CTRL : Modifier::NONE;
			r |= f.Test(Modifier::RIGHT_CTRL) ? Modifier::RIGHT_CTRL : Modifier::NONE;
			r |= f.Test(Modifier::LEFT_ALT) ? Modifier::LEFT_ALT : Modifier::NONE;
			r |= f.Test(Modifier::RIGHT_ALT) ? Modifier::RIGHT_ALT : Modifier::NONE;

			return r;
		}
	};
